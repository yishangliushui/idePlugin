package org.yishang.form;

import javax.swing.*;


public class MybatisGenerator {

	private JPanel mainPanel;
	private JTextArea generatorTextArea;
	private JButton generatorButton;


	public JPanel getMainPanel() {
		return mainPanel;
	}

	public void setMainPanel(JPanel mainPanel) {
		this.mainPanel = mainPanel;
	}

	public JTextArea getGeneratorTextArea() {
		return generatorTextArea;
	}

	public void setGeneratorTextArea(JTextArea generatorTextArea) {
		this.generatorTextArea = generatorTextArea;
	}

	public JButton getGeneratorButton() {
		return generatorButton;
	}

	public void setGeneratorButton(JButton generatorButton) {
		this.generatorButton = generatorButton;
	}
}
